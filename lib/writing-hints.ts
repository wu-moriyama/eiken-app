export const EMAIL_HINTS: Record<string, string> = {
  "3級": `【英検3級 Eメールの型】

■ 基本構成（15〜25語）
1. 挨拶「Thank you for your e-mail.」
2. 質問1への回答
3. 質問2への回答
4. 締め「I hope to see you soon.」など

■ 書き方のコツ
・友達の2つの質問に両方答えよう
・1問につき1〜2文で簡潔に
・カジュアルな表現でOK
・語数を数えて15〜25語に収める`,

  "準2級": `【英検準2級 Eメールの型】

■ 基本構成（40〜50語）
1. 挨拶「Thank you for your e-mail.」
2. 質問への回答（意見＋簡潔な理由）
3. 下線部への質問を2つ書く
4. 締め「Best wishes,」

■ 書き方のコツ
・メールの最後の質問にまず答える
・下線部の「特徴」を問う具体的な質問を2つ書く
　例：下線が「時間」なら「何時からですか？」など
・質問は疑問文で、相手に答えてもらう形に
・語数は40〜50語を目安に`,
};

export function getWritingHint(
  type: WritingHintType,
  level: string
): string {
  const hints = type === "essay" ? ESSAY_HINTS : EMAIL_HINTS;
  return hints[level] ?? hints["3級"] ?? "";
}
